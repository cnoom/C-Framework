#if UNITY_EDITOR
using System.IO;
using System.Text;
using UnityEditor;
using UnityEngine;

namespace CFramework.Editor
{
    /// <summary>
    ///     音频分组枚举代码生成器
    ///     <para>扫描 AudioMixer 的 Group 层级，生成强类型 AudioGroup 枚举</para>
    ///     <para>菜单：Tools > CFramework > Generate AudioGroup Enum</para>
    /// </summary>
    public static class AudioGroupGenerator
    {
        private const string OutputPath = "Assets/Scripts/Generated/AudioGroup.cs";
        private const string Namespace = "CFramework";

        [MenuItem("Tools/CFramework/Generate AudioGroup Enum")]
        public static void Generate()
        {
            // 查找项目中的 AudioMixer
            var mixerGuids = AssetDatabase.FindAssets("t:AudioMixer");
            if (mixerGuids.Length == 0)
            {
                EditorUtility.DisplayDialog("AudioGroup Generator",
                    "未找到 AudioMixer 资源，请先创建 AudioMixer。", "OK");
                return;
            }

            // 如果有多个 Mixer，让用户选择
            AudioMixer selectedMixer = null;
            if (mixerGuids.Length == 1)
            {
                var path = AssetDatabase.GUIDToAssetPath(mixerGuids[0]);
                selectedMixer = AssetDatabase.LoadAssetAtPath<AudioMixer>(path);
            }
            else
            {
                // 显示选择弹窗
                var options = new string[mixerGuids.Length];
                var mixers = new AudioMixer[mixerGuids.Length];
                for (int i = 0; i < mixerGuids.Length; i++)
                {
                    var assetPath = AssetDatabase.GUIDToAssetPath(mixerGuids[i]);
                    options[i] = Path.GetFileName(assetPath);
                    mixers[i] = AssetDatabase.LoadAssetAtPath<AudioMixer>(assetPath);
                }

                // 简单选择逻辑：弹出选择窗口
                var selectedIndex = EditorUtility.DisplayDialogComplex(
                    "AudioGroup Generator",
                    "找到多个 AudioMixer，请选择要使用的 Mixer：\n" +
                    string.Join("\n", options),
                    options[0],
                    options.Length > 1 ? options[1] : "",
                    options.Length > 2 ? options[2] : "");

                if (selectedIndex >= 0 && selectedIndex < mixers.Length)
                    selectedMixer = mixers[selectedIndex];
            }

            if (selectedMixer == null)
            {
                Debug.LogWarning("[AudioGroupGenerator] No AudioMixer selected.");
                return;
            }

            GenerateFromMixer(selectedMixer);
        }

        /// <summary>
        ///     传入指定 Mixer 生成枚举
        /// </summary>
        public static void GenerateFromMixer(AudioMixer mixer)
        {
            if (mixer == null)
            {
                Debug.LogWarning("[AudioGroupGenerator] Mixer is null.");
                return;
            }

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated> 由 AudioGroupGenerator 根据 AudioMixer 生成，请勿手动修改 </auto-generated>");
            sb.AppendLine($"// 生成时间: {System.DateTime.Now:yyyy-MM-dd HH:mm:ss}");
            sb.AppendLine($"// Mixer: {mixer.name}");
            sb.AppendLine();
            sb.AppendLine("using System.ComponentModel;");
            sb.AppendLine();
            sb.AppendLine($"namespace {Namespace}");
            sb.AppendLine("{");
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    ///     音频分组枚举（编辑器自动生成）");
            sb.AppendLine("    ///     <para>值 = 路径哈希(Animator.StringToHash)，用于运行时 O(1) 查找</para>");
            sb.AppendLine("    ///     <para>生成菜单：Tools > CFramework > Generate AudioGroup Enum</para>");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public enum AudioGroup");
            sb.AppendLine("    {");

            // 递归收集所有 Group
            var rootGroups = mixer.FindMatchingGroups("Master");
            var first = true;
            foreach (var root in rootGroups)
                BuildEnumEntries(sb, root, "Master", ref first);

            sb.AppendLine();
            sb.AppendLine("    }");
            sb.AppendLine("}");

            // 写入文件
            var dir = Path.GetDirectoryName(OutputPath);
            if (!Directory.Exists(dir)) Directory.CreateDirectory(dir!);
            File.WriteAllText(OutputPath, sb.ToString());
            AssetDatabase.Refresh();

            Debug.Log($"[AudioGroupGenerator] Generated {OutputPath}");
            EditorUtility.DisplayDialog("AudioGroup Generator",
                $"枚举文件已生成:\n{OutputPath}", "OK");
        }

        private static void BuildEnumEntries(StringBuilder sb, AudioMixerGroup group,
            string path, ref bool first)
        {
            // 枚举成员名 = 路径中 / 替换为 _
            var enumName = path.Replace('/', '_');
            // 哈希值 = Animator.StringToHash(path)
            var hash = Animator.StringToHash(path);

            if (!first) sb.AppendLine(",");
            first = false;

            sb.Append($"        [Description(\"{path}\")]");
            sb.Append($"\n        {enumName} = {hash}");

            // 递归子分组
            foreach (var child in group.children)
            {
                var childPath = $"{path}/{child.name}";
                BuildEnumEntries(sb, child, childPath, ref first);
            }
        }
    }
}
#endif
